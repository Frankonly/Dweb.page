export default class FileType {
  static imageTypes() {
    return ['png', 'jpg', 'jpeg', 'gif', 'svg', 'bmp', 'webp', 'tiff'];
  }

  static videoTypes() {
    return ['mp4', 'avi', 'wmv', 'flv', 'mov', 'webm'];
  }

  static musicTypes() {
    return ['mp3', 'wma', 'ogg', 'wav', 'acc', 'flac'];
  }
}
